      $(function(){
    $(window).scroll(function (){
        $('.fadein').each(function(){
            var position = $(this).offset().top;
              scroll = $(window).scrollTop();
                windowHeight = $(window).height();
            if (scroll > position - windowHeight + 80){
              $(this).addClass('active');
            }
          });
